    //Show Table
    private void showTable(){
        DefaultTableModel model = (DefaultTableModel) userTable.getModel();
        int rowsTable = model.getRowCount();
        for (int i = rowsTable-1;i>=0;i--){
            model.removeRow(i);
        }
        for (int i=1;i<user.rowCount();i++){
             String[] dataRow = user.users()[i];
             model.addRow(dataRow);
        }
    }
