    private void deleteBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_deleteBtnMouseClicked
        
        if (userTable.getSelectedRowCount()==0){
             
            JOptionPane.showMessageDialog(rootPane,"Select Row First!","Error", JOptionPane.ERROR_MESSAGE);
            
        }
        else{
            int results = JOptionPane.showConfirmDialog(rootPane,"Do you want to Delete?","Conformation",JOptionPane.OK_CANCEL_OPTION);
            if (results==JOptionPane.OK_OPTION){
                admin.userDelete(userTable.getSelectedRow()+1);
                showTable();
                clrAddUser();
            }
            
        }
        
    }//GEN-LAST:event_deleteBtnMouseClicked
