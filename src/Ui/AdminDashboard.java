    private void saveBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_saveBtnMouseClicked
        
        String passwordString = new String(passwordTxt.getPassword());
        if (userNameTxt.getText().isEmpty()||passwordString.isEmpty()||nameTxt.getText().isEmpty()||designationCombo.getSelectedItem().equals("Select Destination")){
            JOptionPane.showMessageDialog(rootPane,"All field Required!!","Error", JOptionPane.ERROR_MESSAGE);
        } else if(admin.creteUserAccounts(userNameTxt.getText(), passwordString, nameTxt.getText(), designationCombo.getSelectedItem().toString())){
            JOptionPane.showMessageDialog(rootPane,"Create Account Successfully","Succuessful", JOptionPane.OK_OPTION);
            showTable();
            clrAddUser();
        }else{
            JOptionPane.showMessageDialog(rootPane,"This User Name Already Assigend!!","Error", JOptionPane.ERROR_MESSAGE);
        }
        
    }//GEN-LAST:event_saveBtnMouseClicked
